				<h2>Presentation</h2>
					<div id="presentationboite">
					<p>Since September 2014 I have been a ma&icirc;tre de conf&eacute;rences, at <a href="http://www.math.u-psud.fr/">Laboratoire de Math&eacute;matiques d'Orsay</a> (Universit&eacute; Paris Sud).
					I am a member of the team <a href="http://www.math.u-psud.fr/stats/NEW/stats.php">Probability and statistics</a>.</p>

<p>I am interested in probability and statistics applied to ecology.<br>
					In particular, I study the respective roles of demography and mating systems, on Darwinian evolution. To this aim, I study diploid 
					multi-type logistic birth-and-death processes, and their convergence under different scalings of time, population size, and demographic parameters. With <a href="http://www.cmap.polytechnique.fr/spip.php?rubrique61">Sylvie M&eacute;l&eacute;ard</a> (&Eacute;cole Polytechnique), I consider a diploid Fleming-Viot type superprocess with variable population size.

<br>Besides, I am presently studying the estimation of species abundances with the help of data coming from citizen science programs, such as <a href="http://vigienature.mnhn.fr/‎">Vigie Nature</a> and <a href="www.faune-aquitaine.org/‎">Faune Aquitaine</a>. In this context, I work with <a href="http://www.researchgate.net/profile/Clement_Calenge‎">Cl&eacute;ment Calenge</a> (ONCFS), <a href="http://www.cmapx.polytechnique.fr/~giraud/">Christophe Giraud</a> (U. Paris Sud) and <a href="http://www.researchgate.net/profile/Romain_Julliard‎">Romain Julliard</a> (MNHN).</p>

					<p>I did my Phd thesis at <a href="http://www.cmap.polytechnique.fr/">Centre de Math&eacute;matiques Appliqu&eacute;es</a> of 
					&Eacute;cole Polytechnique, under the supervision of
					<a href="http://www.cmap.polytechnique.fr/spip.php?rubrique61">
					Sylvie M&eacute;l&eacute;ard</a>. My Phd defence occurred on December 2013, 6th, at &Eacute;cole Polytechnique  (<a href="SoutenanceCoron.pdf">Slides of the presentation</a>). 
					My thesis is entitled "Random modeling and eco-evolution of a diploid population"  (<a href="These.pdf">manuscript</a>). During one year I was a Hadamard Lecturer, at <a href="http://www.math.u-psud.fr/">Laboratoire de Math&eacute;matiques d'Orsay</a>.</p>
					
										
					<p>I am a member of the <a href="http://www.cmap.polytechnique.fr/chaire-mmb/">Chaire Mod&eacute;lisation Math&eacute;matique et 
					Biodiversit&eacute;</a>.</p>
					
